// Helper function to get all brand names
export const getBrandNames = (): string[] => {
  return CAR_BRANDS.map(brand => brand.name).sort();
};

// Helper function to get models for a specific brand
export const getModelsForBrand = (brandName: string): string[] => {
  const brand = CAR_BRANDS.find(b => b.name.toLowerCase() === brandName.toLowerCase());
  if (!brand) return [];
  return brand.models.map(m => m.name).sort();
};

// Helper function to search brands
export const searchBrands = (query: string): string[] => {
  const lowerQuery = query.toLowerCase();
  return CAR_BRANDS
    .filter(brand => brand.name.toLowerCase().includes(lowerQuery))
    .map(brand => brand.name);
};

// Helper function to search models within a brand
export const searchModels = (brandName: string, query: string): string[] => {
  const brand = CAR_BRANDS.find(b => b.name.toLowerCase() === brandName.toLowerCase());
  if (!brand) return [];
  const lowerQuery = query.toLowerCase();
  return brand.models
    .filter(model => model.name.toLowerCase().includes(lowerQuery))
    .map(model => model.name);
};
